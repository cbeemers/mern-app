                                       	<p><span style="font-weight: 400;"><strong>One of the most pernicious tools</strong> that Silicon Valley uses to control the flow of information to the public is decidedly low-tech: briefing reporters “on background.” </span></p>
<p><span style="font-weight: 400;">According to the Associated Press, an on background arrangement with a reporter means that “information can be published but only under conditions negotiated with the source. Generally, the sources do not want their names published but will agree to a description of their position.” </span></p>
<p><span style="font-weight: 400;">The AP also recommends that its reporters “object vigorously when a source wants to brief a group of reporters on background” and that they “try to persuade the source to put the briefing on the record.” It laments that “these background briefings have become routine in many venues, especially with government officials.”</span></p>
<p><span style="font-weight: 400;">I’ve been a tech journalist for a decade. I was a senior editor at Motherboard for about four years, and have written and edited stories for outlets including </span><i><span style="font-weight: 400;">Wired</span></i><span style="font-weight: 400;">, </span><i><span style="font-weight: 400;">The Atlantic</span></i><span style="font-weight: 400;">, and Gizmodo. “On background” has been a scourge throughout my career. Every single conversation I have had with a big-five tech company representative this year has been on background. It has become the default method by which Silicon Valley disseminates information to reporters. </span></p>
<p><span style="font-weight: 400;">This is a toxic arrangement. The tactic shields tech companies from accountability. It allows giants like Amazon and Tesla an opportunity to transmit their preferred message, free of risk, in the voice of a given publication. It leaves no trace of policy that might later be criticized—that could form part of the public record to be scrutinized by regulators, lawyers, or investors. If the company later reverses course or modifies its position, the egg is on the reporter’s face, not the company’s. </span></p>
<p><span style="font-weight: 400;">Corporations such as Apple, Google, and Uber have become infamous for their secrecy and unwillingness to comment on most matters on the record. And tech reporters, myself very much included, have not done enough to push them to do otherwise.</span></p>
<div id="mc_embed_signup" class="hidden-print"><center><span class="form-title-embed">Sign up for weekly emails from the <span class="cjr-bold">United States Project</span></span><form action="//cjr.us3.list-manage.com/subscribe/post?u=a23440a018c7ba0619c6f01e6&amp;id=6f9f61ca94" method="post" id="mc-embedded-subscribe-form" name="mc-embedded-subscribe-form" class="validate" target="_blank" novalidate><input type="email" value="" name="EMAIL" class="email" id="mce-EMAIL" placeholder="Email address" required=""><input type="submit" value="SUBSCRIBE" name="subscribe" id="mc-embedded-subscribe" class="button"><div style="position: absolute; left: -5000px;"><input type="text" name="b_a23440a018c7ba0619c6f01e6_c6de9a59ea" value=""></div></form></center></div><p><span style="font-weight: 400;">A recent YouTube controversy, however, presented signs that the ground could shift. For years, the right-wing YouTube personality Steven Crowder had harassed Vox Media’s Carlos Maza, often using homophobic slurs. In the face of criticism, YouTube, which has a hair-trigger sensitivity to the accusation, made by conservatives, that it is anti-conservative, declared that Crowder was not in violation of its harassment policies. But the company refused to address the issue on the record. It sought instead to brief reporters on background. </span></p>
<p><strong>ICYMI: <a href="https://www.cjr.org/analysis/carlos-maza-youtube-steven-crowder.php">During Pride Month, YouTube shows its true colors</a></strong></p>
<p><span style="font-weight: 400;">The Verge’s editor-in-chief, Nilay Patel, tweeted that he had <a href="https://webcache.googleusercontent.com/search?q=cache:W3XBkv6_Ei0J:https://twitter.com/reckless/status/1136088139070443521+&amp;cd=12&amp;hl=en&amp;ct=clnk&amp;gl=ca">chosen to ignore</a> YouTube’s explanation to his team because the spokesperson refused to be quoted on the record about how and why they had </span><a href="https://www.theverge.com/2019/6/4/18653088/youtube-steven-crowder-carlos-maza-harassment-bullying-enforcement-verdict"><span style="font-weight: 400;">chosen not to enforce their harassment policies</span></a><span style="font-weight: 400;"> in this case. </span></p>
<p><span style="font-weight: 400;">Meanwhile, at Gizmodo, </span><a href="https://gizmodo.com/youtube-no-we-wont-remove-these-videos-of-racist-ant-1835259054"><span style="font-weight: 400;">YouTube responded</span></a><span style="font-weight: 400;"> to reporter Tom McKay’s request for an explanation with a note that included, “Further info on Background (okay to paraphrase, according to YouTube).” Because McKay had made no such agreement with the company beforehand, he published the statement in full. (Note that YouTube was so predisposed to on-background arrangements that it simply assumed McKay would automatically honor this one.) </span></p>
<p><span style="font-weight: 400;">Both moves were the right ones. I hope they might help build momentum to blunt the dominant and long-held expectation in Silicon Valley that tech companies can dole out information about their most important policy decisions—regarding platforms and products that impact hundreds of millions of people—on terms that they alone dictate. </span></p>
<p><span style="font-weight: 400;">It’s about time we object more vigorously to the normalization of this tactic.</span></p>
<p>&nbsp;</p>
<p><span style="font-weight: 400;"><strong>SILICON VALLEY WASN&#8217;T ALWAYS SO HOSTILE</strong> to reporters. It used to be relatively open. Apple, probably more than any other company, snapped it closed. In my book about the history of the iPhone, </span><em>The One Device</em><span style="font-weight: 400;">, I dedicate a chapter to the company&#8217;s marketing prowess. Keeping the press at arm’s length was a key part of its strategy. </span></p>
<p><span style="font-weight: 400;">The most significant change was Steve Jobs’ return to Apple in the &#8217;90s. At this time he began to methodically clamp down on press access. Reporters such as </span><a href="https://www.nytimes.com/2004/04/25/business/oh-yeah-he-also-sells-computers.html"><span style="font-weight: 400;">John Markoff</span></a><span style="font-weight: 400;"> and </span><a href="https://www.amazon.com/Becoming-Steve-Jobs-Evolution-Visionary/dp/0385347421"><span style="font-weight: 400;">Brendt Schendler</span></a><span style="font-weight: 400;"> have documented how Jobs restricted the flow of information from Apple—only select executives, himself included, were permitted to speak to select media outlets, and engineers and higher-level employees were forbidden from going on the record. </span></p>
<p><span style="font-weight: 400;">This controlled access strategy was in force when Apple released the iPod, iPhone, and iPad, and rose to become the most powerful consumer technology company on the planet. (One former executive told me that the secrecy around the iPhone was worth hundreds of millions of dollars.) It created such a booming demand for this scarce information that a cottage knowledge industry sprang up, with reporters and bloggers competing to break news about items like product update announcements and leaked supply chain specs. Apple learned that it wouldn’t have to open its doors to critics to get its message out—most of the blogging was done by superfans, after all, and Silicon Valley was still enjoying a halo of public goodwill.</span></p>
<p><span style="font-weight: 400;">Other tech companies followed suit. And as Silicon Valley found itself in an ever-expanding position of power, journalism was contracting. Even some of the most prestigious publications grew desperate. If one outlet wouldn’t agree to onerous terms, another, keen for the web traffic new information brings, would. The usual method for combating this—deeply sourced reporters who have the time and trust to miss a few news breaks in order to focus on independent reporting—required too much confidence to sustain. As such, de facto on background stipulations were free to proliferate. </span></p>
<p><span style="font-weight: 400;">Here are two recent examples of what this looks like in practice. Tesla’s Autopilot, the semi-automated driving software that ships with its vehicles, has been under serious scrutiny after it was found </span><a href="https://www.wired.com/story/tesla-autopilot-self-driving-crash-california/"><span style="font-weight: 400;">enabled during fatal crashes</span></a><span style="font-weight: 400;">—in one case, Autopilot steered its driver into a white truck it did not detect; in another, the car collided with a concrete highway lane divider. In the wake of those incidents, I wrote about Tesla’s decision to </span><a href="https://gizmodo.com/the-deadly-recklessness-of-the-self-driving-car-industr-1831027948"><span style="font-weight: 400;">promote their vehicles</span></a><span style="font-weight: 400;"> as equipped with “full self-driving hardware.” </span></p>
<p><span style="font-weight: 400;">Dr. Jack Stilgoe, a Turing Institute fellow and senior lecturer at University College London, told me at the time that the language was irresponsible and misleading—it was unethical to lure customers in with promises of cars with “full self-driving hardware” when Autopilot in reality enabled only semi-autonomous steering, and required hands placed on the wheel at all times. I asked Tesla to respond. </span></p>
<p><span style="font-weight: 400;">The PR team agreed to talk, but only on background; I could not quote anything said in the conversation. They denigrated Stilgoe’s work and offered me a test-drive so I could see how safe their cars were. None of this, of course, addressed the issue at hand—that their marketing materials, and statements on TV from the company’s founder Elon Musk, were dangerously misleading. They did say that salespeople made the distinction clear on the dealership floors, but would not allow me to quote that. </span></p>
<p><span style="font-weight: 400;">The conversation was essentially a targeted sales pitch—albeit one with strange tactics, as they spent a lot of time basically yelling at me—that was aimed at convincing me that Autopilot was safe. At one point, they told me, “it sounds like you need to rethink your story.” </span></p>
<p><span style="font-weight: 400;">In hindsight, I regret including any of the information from that tortured conversation in the final piece. If Tesla wants to issue a statement contesting the analysis of a technology scholar that concludes their language is misleading they should do it on the record or they should walk back their marketing materials. (Months later, in fact, they </span><a href="https://twitter.com/bcmerchant/status/1134216931521601536"><span style="font-weight: 400;">quietly did exactly that</span></a><span style="font-weight: 400;">.)</span></p>
<p><span style="font-weight: 400;">Another example. Recently I wrote </span><a href="https://gizmodo.com/amazon-is-aggressively-pursuing-big-oil-as-it-stalls-ou-1833875828"><span style="font-weight: 400;">a story</span></a><span style="font-weight: 400;"> which showed that Amazon, which has made a commitment to powering its operations with clean energy, had stalled on those commitments while also aggressively pursuing deals and partnerships with oil and gas companies. </span></p>
<p><span style="font-weight: 400;">The company ignored my request for a comment until the story ran, then announced two new clean energy projects and offered to speak, on background, about the development. To me, it seemed that they had used my request as a heads-up to announce something that would make my story appear off-base. Of course, I can neither confirm nor deny this, because Amazon only offered to speak on background—and I refused. </span></p>
<p><span style="font-weight: 400;">These companies are too big and too influential to be allowed to negotiate the nature of truth with journalists out of sight of readers. After my experience with Amazon, I decided that on all matters of importance, I am no longer going to listen to a public relations representative try to change my mind on background with unquotable statements attributable to no one. No reporter should, not when the stakes are as high as they are. If an actual source—an engineer, or a policymaker—wants to go on background for protection, that’s one thing. But a spokesperson should either go on the record or get off the phone. </span></p>
<p><span style="font-weight: 400;">Good, aggressive reporters are regularly pushing back against these ossified norms. We all should join them, and now. Tech PR shops exist solely to paint their employers in a flattering light; they can do so on the record.</span></p>
